using System;
using System.Collections.Generic;
using System.Text;

namespace DatabaseLibrary.Models
{
    public class Client_db
    {

        #region Constructors

        /// <summary>
        /// Default constructor.
        /// </summary>
        public Client_db()
        {
        }

        /// <summary>
        /// Fields constructor.
        /// </summary>
        public Client_db(int id, DateTime dob, string name, double weight, double height, double waistCircumference, double hipCircumference, double neckCircumference)
        {
            Id = id;
            Dob = dob;
            Name = name;
            Weight = weight;
            Height = height;
            WaistCircumference = waistCircumference;
            HipCircumference = hipCircumference;
            NeckCircumference = neckCircumference;
        }

        #endregion

        #region Properties

        /// <summary>
        /// Id generated by our system upon creation of a new instance.
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// Date of birth of client
        /// </summary>
        public DateTime Dob { get; set; }

        /// <summary>
        /// First name used of the client.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Weight of the client.
        /// </summary>
        public double Weight { get; set; }

        /// <summary>
        /// Height of the client.
        /// </summary>
        public double Height { get; set; }

        /// <summary>
        /// Waist Circumference of the client.
        /// </summary>
        public double WaistCircumference { get; set; }

        /// <summary>
        /// Hip Circumference of the client.
        /// </summary>
        public double HipCircumference { get; set; }

        /// <summary>
        /// Neck Circumference of the client.
        /// </summary>
        public double NeckCircumference { get; set; }

        #endregion

    }
}
